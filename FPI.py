class FuzzySystem:

    def __init__(self, name):
        self.name = name
        self.rules = []

    def trap_mf(self, x, a, b, c, d):
        if x <= a:
            return 0
        elif a < x < b:
            return (x - a) / (b - a)
        elif b <= x <= c:
            return 1
        elif c < x < d:
            return (d - x) / (d - c)
        else:
            return 0

    def trimf(self, x, a, b, c):
        if x <= a:
            return 0
        elif a < x <= b:
            return (x - a) / (b - a)
        elif b < x < c:
            return (c - x) / (c - b)
        else:
            return 0

    def fuzzify(self, value, mfs):
        return {name: mf(value) for name, mf in mfs.items()}

    def evaluate_rules(self, inputs_fuzzy, rules, output_range, output_mfs):
        output_values = []

        for x in output_range:
            rule_activations = []

            for rule in rules:
                input_indices = rule[:-1]
                output_idx = rule[-1]

                activation = 1.0
                valid_inputs = [v for v in input_indices if v != 0]

                if len(valid_inputs) != len([k for k in inputs_fuzzy.keys()]):
                    continue

                for i, idx in enumerate(valid_inputs):
                    input_name = list(inputs_fuzzy.keys())[i]
                    term_names = list(inputs_fuzzy[input_name].keys())
                    if idx - 1 < len(term_names):
                        term = term_names[idx - 1]
                        membership = inputs_fuzzy[input_name][term]
                        activation = min(activation, membership)

                out_term_names = list(output_mfs.keys())
                if output_idx - 1 < len(out_term_names):
                    out_term = out_term_names[output_idx - 1]
                    out_membership = output_mfs[out_term](x)
                    rule_activation = min(activation, out_membership)
                    rule_activations.append(rule_activation)

            if rule_activations:
                output_values.append(max(rule_activations))
            else:
                output_values.append(0)

        numerator = sum(x * y for x, y in zip(output_range, output_values))
        denominator = sum(output_values)

        if denominator == 0:
            return 0
        return numerator / denominator


